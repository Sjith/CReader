package com.sogou.data;

public class book_mark {
	private String book_name;
	private String author_name;
	private String begin_buf;
	private String update_time;
	private int chapter_num;
	private String percent;
	private int type;
	
	public String getBook_name() {
		return book_name;
	}
	public void setBook_name(String book_name) {
		this.book_name = book_name;
	}
	public String getAuthor_name() {
		return author_name;
	}
	public void setAuthor_name(String author_name) {
		this.author_name = author_name;
	}
	public String getBegin_buf() {
		return begin_buf;
	}
	public void setBegin_buf(String begin_buf) {
		this.begin_buf = begin_buf;
	}
	public String getUpdate_time() {
		return update_time;
	}
	public void setUpdate_time(String update_time) {
		this.update_time = update_time;
	}
	public int getChapter_num() {
		return chapter_num;
	}
	public void setChapter_num(int chapter_num) {
		this.chapter_num = chapter_num;
	}
	public String getPercent() {
		return percent;
	}
	public void setPercent(String percent) {
		this.percent = percent;
	}
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	
}
